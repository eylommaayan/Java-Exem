 public class Main {
    public static void main(String[] args) {
        int[] a = {1, 2, 5, 3, 6, 10, 9};
        int x = 10;
        System.out.println(findX(a, x));
    }

    public static boolean findX(int[] a, int x) {
        int low = 0, high = a.length - 1, mid = 0;
        if (a.length == 1) {
            return false;
        }
        while (low < high) {
            mid = (low + high) / 2;
            if (a[mid] + a[mid + 1] == x) {
                return true;
            }
            if (a[mid] + a[mid + 1] < x) {
                low = mid + 1;
            } else {
                high = mid;
            }
        }
        return false;
    }
}


