public static int countTriplets(int[] arr, int num) {
    int count = 0; // משתנה לספירת כמות הטריפלטים שסכומם קטן או שווה לערך הנתון
    
    // לולאה ראשית שעוברת על כל איבר במערך עד האיבר השלישי מהסוף
    for (int i = 0; i < arr.length - 2; i++) {
        int j = i + 1; // מצביע לתחילת החלק השני של הטריפלט
        int k = arr.length - 1; // מצביע לסוף המערך, לסיום הטריפלט
        
        // לולאה פנימית שעוברת על כל זוג אפשרי מהנקודה 'i' עד סוף המערך
        while (j < k) {
            // אם סכום הטריפלט קטן או שווה לערך הנתון
            if (num <= arr[i] + arr[j] + arr[k]) {
                k--; // נזיז את המצביע 'k' אחורה כדי לבדוק טריפלט קטן יותר
            } else {
                count += (k - j); // נוסיף לספירה את כל הטריפלטים האפשריים בין 'j' ל-'k'
                j++; // נזיז את המצביע 'j' קדימה כדי לבדוק טריפלט חדש
            }
        }
    }
    return count; // נחזיר את כמות הטריפלטים שנמצאו
}
